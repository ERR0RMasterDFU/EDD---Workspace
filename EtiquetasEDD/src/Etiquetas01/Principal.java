public class Principal {

		/**
		 * Este método sirve para sumar dos números.
		 * @param num1 es un número (entro o decimal).
		 * @param num2 es un número (entro o decimal).
		 * Devuelve la suma de los dos números anteriores.
		 */
		
		public double suma(double num1, double num2) {
			 return num1+num2;
		 }
		
		
		/**
		 * Este método sirve para restar dos números.
		 * @param num1 es un número (entro o decimal).
		 * @param num2 es un número (entro o decimal).
		 * Devuelve la resta de los dos números anteriores.
		 */
		
		public double resta(double num1, double num2) {
			 return num1-num2;
		 }
		
		
		/**
		 * Este método sirve para multiplicar dos números.
		 * @param num1 es un número (entro o decimal).
		 * @param num2 es un número (entro o decimal).
		 * Devuelve la multiplicación de los dos números anteriores.
		 */
		
		public double multiplicar(double num1, double num2) {
			 return num1*num2;
		 }
		
		
		
		
		
		
	}